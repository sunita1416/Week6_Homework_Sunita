package javaprogrammes;

public class SwapTwoVariables {
    public static void main(String[] args) {
        //Variable
        int x, y;
        int temp;
        x = 50;
        y = 100;
        temp = x; //Variable to swap
        x = y; // 100
        y = temp; // 50
        //Print method with swap formula
        System.out.println("x = " + x);
        System.out.println("y = " + y);





    }



}
